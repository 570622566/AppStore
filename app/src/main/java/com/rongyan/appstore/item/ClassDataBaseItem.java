package com.rongyan.appstore.item;

/**
 * 数据库基本类
 */

public class ClassDataBaseItem {

    private int id;

    private String value;

    private int time;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}
